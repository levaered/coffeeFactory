class Mocha extends Coffee {
    public Mocha(int sugarSpoons, String coffeType) {
        countOfSugar = sugarSpoons;
        typeOfCoffe = coffeType;
        name = "Mocha";
        ingredients.add("50% - milk");
        ingredients.add("25% - coffee");
        ingredients.add("25% - dark chocolate");
        ingredients.add("whipped cream");
    }
}
